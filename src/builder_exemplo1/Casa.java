package builder_exemplo1;

public class Casa {
	
	private String tipoParede;
	private Telhado telhado;
	private Acabamento acabamento;
	
	public String getTipoParede() {
		return tipoParede;
	}
	public void setTipoParede(String tipoParede) {
		this.tipoParede = tipoParede;
	}
	public Telhado getTelhado() {
		return telhado;
	}
	public void setTelhado(Telhado telhado) {
		this.telhado = telhado;
	}
	public Acabamento getAcabamento() {
		return acabamento;
	}
	public void setAcabamento(Acabamento acabamento) {
		this.acabamento = acabamento;
	}
	
	

}
